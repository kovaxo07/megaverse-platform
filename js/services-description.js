// Service Description Display Functions
function displayServiceDescription(serviceType) {
    const service = MegaverseServices[serviceType];
    if (!service) return '';

    let html = `
        <div class="service-description">
            <h2>${service.title}</h2>
            <p class="service-overview">${service.description}</p>
    `;

    // Add features based on service type
    if (service.features) {
        html += `<div class="features-grid">`;
        service.features.forEach(feature => {
            html += `
                <div class="feature-card">
                    <h3>${feature.name}</h3>
                    <p>${feature.details}</p>
                </div>
            `;
        });
        html += `</div>`;
    }

    // Add Web3 features for casino
    if (service.web3Features) {
        html += `<div class="web3-features">`;
        service.web3Features.forEach(feature => {
            html += `
                <div class="web3-card">
                    <h4>${feature.title}</h4>
                    <p>${feature.description}</p>
                </div>
            `;
        });
        html += `</div>`;
    }

    html += `</div>`;
    return html;
}

// Initialize service descriptions on page load
document.addEventListener('DOMContentLoaded', function() {
    // This will be used by individual pages to display their service descriptions
    console.log('Megaverse Services Description System Loaded');
    
    // Auto-populate service descriptions on respective pages
    const pageServiceMap = {
        'casino.html': 'casino',
        'arcade.html': 'arcade',
        'arena.html': 'arena',
        'theater.html': 'theater',
        'mall.html': 'mall',
        'community.html': 'community'
    };

    const currentPage = window.location.pathname.split('/').pop();
    const serviceType = pageServiceMap[currentPage];
    
    if (serviceType) {
        const serviceContainer = document.getElementById('service-description-container');
        if (serviceContainer) {
            serviceContainer.innerHTML = displayServiceDescription(serviceType);
        }
    }
});
